#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int	main(int a, char **c)
{
	int	i;

	i = 0;
	if (a == 0)
		return (0);
	while (c[0][i])
		ft_putchar(c[0][i++]);
}
